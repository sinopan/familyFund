package com.sinoyoo.familyfunds.utils;

import org.apache.poi.hssf.usermodel.HSSFCell;
import org.apache.poi.hssf.usermodel.HSSFCellStyle;
import org.apache.poi.hssf.usermodel.HSSFFont;
import org.apache.poi.hssf.usermodel.HSSFRow;
import org.apache.poi.hssf.usermodel.HSSFSheet;
import org.apache.poi.hssf.usermodel.HSSFWorkbook;
import org.apache.poi.ss.usermodel.BorderStyle;
import org.apache.poi.ss.usermodel.IndexedColors;

public class ExcelUtil {

    /**
     * 导出Excel
     * @param sheetName sheet名称
     * @param title 标题
     * @param values 内容
     * @param wb HSSFWorkbook对象
     * @return
     */
    public static HSSFWorkbook getHSSFWorkbook(String sheetName,String []title,String [][]values, HSSFWorkbook wb){

        // 第一步，创建一个HSSFWorkbook，对应一个Excel文件
        if(wb == null){
            wb = new HSSFWorkbook();
        }

        // 第二步，在workbook中添加一个sheet,对应Excel文件中的sheet
        HSSFSheet sheet = wb.createSheet(sheetName);

        // 第三步，在sheet中添加表头第0行,注意老版本poi对Excel的行数列数有限制
        HSSFRow row = sheet.createRow(0);

        // 第四步，创建单元格，并设置值表头 设置表头居中
        HSSFCellStyle style = wb.createCellStyle();
        // style.setAlignment(HSSFCellStyle.); // 创建一个居中格式

        //声明列对象
        HSSFCell cell = null;
        
        /*
         * 样式
         */
       /* sheet.setColumnWidth(0, 3000);
        sheet.setColumnWidth(1, 3000);*/
        sheet.setColumnWidth(4, 4000);
        sheet.setColumnWidth(6, 7000);
        
        //设置Excel中的边框(表头的边框)
        //style.setFillBackgroundColor((short) 346729);
        //style.setFillForegroundColor((short) 111111);
        
        
        //设置字体

        HSSFFont font = wb.createFont();
        font.setFontHeightInPoints((short) 30); // 字体高度
        font.setFontName("楷体"); // 字体
        style.setFont(font);
        
        //设置Excel中的背景
        
        //创建标题
        for(int i=0;i<title.length;i++){
            cell = row.createCell(i);
            cell.setCellValue(title[i]);
            
            style = wb.createCellStyle();
            style.setFillForegroundColor(IndexedColors.BLUE_GREY.index);
            style.setFillBackgroundColor(IndexedColors.AQUA.index);
            cell.setCellStyle(style);
        }

        //创建内容
        for(int i=0;i<values.length;i++){
            row = sheet.createRow(i + 1);
            for(int j=0;j<values[i].length;j++){
                //将内容按顺序赋给对应的列对象
                row.createCell(j).setCellValue(values[i][j]);
            }
        }
        
        return wb;
    }
}